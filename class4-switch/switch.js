let dayno = prompt();
dayno=Number(dayno);
switch (dayno) {
    case 1:
        console.log("Monday")
        break;
  case 2:
        console.log("Tuesday")
        break;
  case 3:
        console.log("wednesday")
        break;
  case 4:
        console.log("Thursday")
        break;
  case 5:
        console.log("friday")
        break;

    default:
        console.log("Please enter the valid day")
        break;
}